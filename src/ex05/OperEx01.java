package ex05;

public class OperEx01 {
    public static void main(String[] args) {
        System.out.println(1 + 1); // 더하기
        System.out.println(1 - 1); // 빼기
        System.out.println(2 * 2); // 곱하기
        System.out.println(2 / 2); // 나누기
        System.out.println(9 % 7); // 나누었을 때 떨어지는 나머지 값

        // 더하기(산술더하기 or 결합)
        System.out.println("1" + 1); // answer is 11, "문자열"+숫자 결합
    }
}
