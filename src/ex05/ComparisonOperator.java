package ex05;

public class ComparisonOperator {
    public static void main(String[] args) {
        int x = 3; // 변수 지정
        int y = 4;
        System.out.println(x == y); // answer is false
        System.out.println(x != y); // answer is true
        System.out.println(x > y); // answer is false
        System.out.println(x < y); // answer is true
        System.out.println(x <= y); // answer is true
    }
}
