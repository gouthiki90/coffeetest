package ex06;

public class Practice6 {
    public static void main(String[] args) {

        for (int y = 0; y < 5; y++) { // 4번까지 y를 1씩 더한다.
            for (int x = 0; x < 10; x++) // 0을 9번까지 1씩 더한다.
                System.out.println("*");
            System.out.println("");

            // 아무래도 이건 버전 문제로 아닌 것 같소
            // 사각형이 *로 찍혀진다는데 한줄만 나오는뎁쇼
        }
    }
}
