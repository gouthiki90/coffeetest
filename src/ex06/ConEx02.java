package ex06;

public class ConEx02 {
    public static void main(String[] args) {
        int x = 25; // 변수 초기화

        if (x < 30) { // 조건 선언
            System.out.println("x는 30 보다 작습니다."); // true일 때 출력
        } else {
            System.out.println("x는 30 보다 큽니다."); // flase일 때 출력
        }
    }
}
